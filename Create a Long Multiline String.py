# Program to Create a Long Multiline String

# Solution 1 Using triple quotes

print('''The only way to
learn to program is
by writing code.''')



# Solution 2  Using \n


print("The only way to \nlearn to program is \nby writing code.")



# Solution 3 Using \

my_string = "The only way to \n" \
        	"learn to program is \n" \
        	"by writing code."

print(my_string)
